public class DataTypesExample {
    public static void main(String[] args) {
        int age=22; // age -> integer
        float PI=3.14F; // temperature -> decimal
        Double salary=55000.75; // salary -> large decimal
        char grade='A'; // grade -> single character
        byte level=5; // very small number
        short year=2025; // small integer
        boolean isStudent=true; // true/false value
        long population=1400000000L; // large number

        System.out.println("Age:"+age);
        System.out.println("PI:"+PI);
        System.out.println("Salary:"+salary);
        System.out.println("Grade:"+grade);
        System.out.println("Level:"+level);
        System.out.println("Year:"+year);
        System.out.println("IsStudent:"+isStudent);
        System.out.println("Population:"+population);
    }
}
